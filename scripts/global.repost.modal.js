const repostedModal = document.getElementById("repostedModal");
const hideRepostedModalBtn = document.getElementById("hideRepostedModalBtn");

repostedModal?.addEventListener("click", (e) => {
  if (e.target.id === "repostedModal") return hideRepostedModal();
});

hideRepostedModalBtn?.addEventListener("click", hideRepostedModal);

function showRepostedModal() {
  repostedModal.classList.remove(HIDDEN);
}

function hideRepostedModal() {
  repostedModal.classList.add(HIDDEN);
}
